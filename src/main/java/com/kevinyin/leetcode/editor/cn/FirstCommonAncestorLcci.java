    class Solution {

        TreeNode ans;

        public TreeNode lowestCommonAncestor(TreeNode root, TreeNode p, TreeNode q) {
            dfs(root, p, q);
            return ans;
        }

        public boolean dfs(TreeNode node, TreeNode p, TreeNode q) {
            if (node == null) {
                return false;
            }
            boolean left = dfs(node.left, p, q);
            boolean right = dfs(node.right, p, q);
            if (node.equals(p) || node.equals(q)) {
                // åœ¨ä¹‹å‰æ‰¾åˆ°çš„åŸºç¡€ä¸Šï¼Œå¦‚æœåˆæ‰¾åˆ°äº†ä¸€ä¸ªï¼Œé‚£å°±æ˜¯å½“å‰èŠ‚ç‚¹äº†
                if (left || right) {
                    ans = node;
                    return false;
                } else {
                    return true;
                }
            } else {
                // åˆšå¥½åœ¨å·¦è¾¹å’Œå³è¾¹éƒ½æ‰¾åˆ°äº†ï¼Œå°±æ˜¯å…¬å…±çš„æ ¹èŠ‚ç‚¹äº†
                if (left && right) {
                    ans = node;
                    return false;
                }
            }
            return left || right;
        }
    }
